package com.lujianfei.icecontroller.model;

public class ConnectionInfo {
	private String addr;
	private int port;
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
}
